#dictionaries are very similar to lists, however, they have a key pair value that simplifies the operations that can 
# be performed. 

#Example:
person = {'name' : 'John Doe', 'age':30, 'location': 'Nairobi', 'email' :'johndoe@mail.com'}
print(person['name'])

#NB: A key cannot be repeated. It's value can be re-assigned.
##: Keys are case sensitive.
##: You can get a list of all keys by using "person.keys()"
##: Keys must always be string
